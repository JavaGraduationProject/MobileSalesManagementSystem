// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isPhone, isMobile,isURL,checkIdCard } from "@/utils/validate";
export default {
	data() {
		let self = this
		var validateIdCard = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!checkIdCard(value)) {
				callback(new Error("请输入正确的身份证号码"));
			} else {
				callback();
			}
		};
		var validateUrl = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isURL(value)) {
				callback(new Error("请输入正确的URL地址"));
			} else {
				callback();
			}
		};
		var validateMobile = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isMobile(value)) {
				callback(new Error("请输入正确的手机号码"));
			} else {
				callback();
			}
		};
		var validatePhone = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isPhone(value)) {
				callback(new Error("请输入正确的电话号码"));
			} else {
				callback();
			}
		};
		var validateEmail = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isEmail(value)) {
				callback(new Error("请输入正确的邮箱地址"));
			} else {
				callback();
			}
		};
		var validateNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isNumber(value)) {
				callback(new Error("请输入数字"));
			} else {
				callback();
			}
		};
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				biaoti : false,
				kehumingcheng : false,
				kehuleixing : false,
				lianxidianhua : false,
				kehuyixiang : false,
				genjinqingkuang : false,
				yonghuming : false,
				xingming : false,
				tianjiashijian : false,
			},
			
			
			ruleForm: {
				biaoti: '',
				kehumingcheng: '',
				kehuleixing: '',
				lianxidianhua: '',
				kehuyixiang: '',
				genjinqingkuang: '',
				yonghuming: '',
				xingming: '',
				tianjiashijian: '',
			},
		
			kehumingchengOptions: [],
			
			rules: {
				biaoti: [
					{ required: true, message: '标题不能为空', trigger: 'blur' },
				],
				kehumingcheng: [
					{ required: true, message: '客户名称不能为空', trigger: 'blur' },
				],
				kehuleixing: [
				],
				lianxidianhua: [
					{ validator: validateMobile, trigger: 'blur' },
				],
				kehuyixiang: [
					{ required: true, message: '客户意向不能为空', trigger: 'blur' },
				],
				genjinqingkuang: [
					{ required: true, message: '跟进情况不能为空', trigger: 'blur' },
				],
				yonghuming: [
				],
				xingming: [
				],
				tianjiashijian: [
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
	created() {
		this.ruleForm.tianjiashijian = this.getCurDateTime()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='biaoti'){
							this.ruleForm.biaoti = obj[o];
							this.ro.biaoti = true;
							continue;
						}
						if(o=='kehumingcheng'){
							this.ruleForm.kehumingcheng = obj[o];
							this.ro.kehumingcheng = true;
							continue;
						}
						if(o=='kehuleixing'){
							this.ruleForm.kehuleixing = obj[o];
							this.ro.kehuleixing = true;
							continue;
						}
						if(o=='lianxidianhua'){
							this.ruleForm.lianxidianhua = obj[o];
							this.ro.lianxidianhua = true;
							continue;
						}
						if(o=='kehuyixiang'){
							this.ruleForm.kehuyixiang = obj[o];
							this.ro.kehuyixiang = true;
							continue;
						}
						if(o=='genjinqingkuang'){
							this.ruleForm.genjinqingkuang = obj[o];
							this.ro.genjinqingkuang = true;
							continue;
						}
						if(o=='yonghuming'){
							this.ruleForm.yonghuming = obj[o];
							this.ro.yonghuming = true;
							continue;
						}
						if(o=='xingming'){
							this.ruleForm.xingming = obj[o];
							this.ro.xingming = true;
							continue;
						}
						if(o=='tianjiashijian'){
							this.ruleForm.tianjiashijian = obj[o];
							this.ro.tianjiashijian = true;
							continue;
						}
				}
				









			}
			
			
			// 获取用户信息
			this.$http({
				url: `${this.$storage.get('sessionTable')}/session`,
				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					
					var json = data.data;
					if(this.$storage.get("role")!="管理员") {
						this.ro.kehuleixing = true;
					}
					if(this.$storage.get("role")!="管理员") {
						this.ro.lianxidianhua = true;
					}
					if(((json.yonghuming!=''&&json.yonghuming) || json.yonghuming==0) && this.$storage.get("role")!="管理员"){
						this.ruleForm.yonghuming = json.yonghuming
						this.ro.yonghuming = true;
					}
					if(((json.xingming!=''&&json.xingming) || json.xingming==0) && this.$storage.get("role")!="管理员"){
						this.ruleForm.xingming = json.xingming
						this.ro.xingming = true;
					}
					if(this.$storage.get("role")!="管理员") {
						this.ro.tianjiashijian = true;
					}
