					if (data && data.code === 0) {
						if(data.data.kehuleixing){
							this.ruleForm.kehuleixing = data.data.kehuleixing
						}
						if(data.data.lianxidianhua){
							this.ruleForm.lianxidianhua = data.data.lianxidianhua
						}
					} else {
						this.$message.error(data.msg);
					}
				});
			},
    // 多级联动参数

    info(id) {
      this.$http({
        url: `baifanggenjin/info/${id}`,
        method: "get"
